/**
 * Get a game by ID
 */
export function getSlotGameById(id: string | number): SlotGame | undefined {
  return ALL_SLOT_GAMES.find(game => game.id === id || game.slug === id);
}

/**
 * Get games by provider
 */
export function getGamesByProvider(provider: string): SlotGame[] {
  return ALL_SLOT_GAMES.filter(game => game.provider.toLowerCase() === provider.toLowerCase());
}

/**
 * Get games with specific badge
 */
export function getGamesByBadge(badge: 'New' | 'Buy Bonus'): SlotGame[] {
  return ALL_SLOT_GAMES.filter(game => game.badges.includes(badge));
}
